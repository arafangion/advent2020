-- Get the n'th line from the map of trees.
line :: Text -> Int -> String
line trees y = unpack (lines trees !! y) <> line trees y

-- Is the given coordinate a tree?
isTree :: Text -> (Int, Int) -> Bool
isTree trees (x, y) = (line trees y) !! x == '#'

-- Produce an infinite list of coordinates along the slope, as per problem
slope :: Int -> Int -> [(Int, Int)]
slope dx dy = iterate (\(x, y) -> (dx + x, dy + y)) (dx, dy)

-- Determine how many lines there are in the map of trees.
countTreeLines trees = length $ lines trees

-- The full set of coordinates we need along the slope.
fullSlope :: Text -> Int -> Int -> [(Int, Int)]
fullSlope trees dx dy = take (((countTreeLines trees )- 1) `div` dy) (slope dx dy)

-- The sequence of tree or not a tree points along the slope...
treeSlopes :: Text -> Int -> Int -> [Bool]
treeSlopes trees dx dy = map (isTree trees) (fullSlope trees dx dy)

-- And how many are there, for the given map of trees and the given slope.
countTrees trees dx dy = Prelude.foldr (\a x -> if a then x + 1 else x) 0 (treeSlopes trees dx dy)

part1 = putStr $ "Day 3 Part 1: " <> (show $ countTrees trees 3 1) <> "\n"
part2 = putStr $ "Day 3 Part 2: " <> (show $ product [ countTrees trees 1 1
            , countTrees trees 3 1
            , countTrees trees 5 1
            , countTrees trees 7 1
            , countTrees trees 1 2
            ]) <> "\n"
main = part1 >> part2
